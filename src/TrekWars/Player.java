package TrekWars;



public class Player {
	private int life;
	private int energy;
	private int score;
	
	public int getlife() {
		return life;
	}
	public void setlife(int life) {
		this.life = life;
	}
	public int getEnergy() {
		return energy;
	}
	public void setEnergy(int energy) {
		this.energy = energy;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	 
}
